package com.kingcode;

public class Play {

    public static int solution(int start, int finish) {
        // 1 -> 2 -> 3 -> 4 -> 5
        Integer x = checkCornerCases(start, finish);
        if (x != 1000) return x;
        int jump = 1;
        if (start + jump == finish) {
            return jump;
        } else if (jump + 3 == finish) {
            return jump;
        }
        jump++;
        do {
            if (jump + 1 == finish) {
                return jump;
            } else if (jump + 3 == finish) {
                return jump;
            }
            jump++;
        } while (jump <= finish);
        return jump;
    }

    private static Integer checkCornerCases(int start, int finish) {
        if (start < 0 || finish <= 1 || finish < start) {
            return -1;
        }
        return 1000;
    }
}
